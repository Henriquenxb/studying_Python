#criação da lista
lista = [1, 7, 7, 19, 3, 2, 11, 15, 6, 1, 5]

#exibição da lista
print(f"A lista foi criada assim: {lista}")

#contagem de elementos
contagem = lista.count(7)
print(f"Nessa lista o número {7} aparece {contagem} vezes")

#índice em que encontrou o valor
indice = lista.index(11)
print(f"O número {11} foi encontrado no índice: {indice}")