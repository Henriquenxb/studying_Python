#func de soma
def somar(a,b): 
    return float(a) + float(b)

#func de subtração
def subtrair(a,b):
    return float(a) - float(b)

#func de divisão
def dividir(a,b):
    if b == 0:
        return 0
    return float(a) / float(b)

#func de multiplicar
def mutiplicar(a,b):
    return float(a) * float(b)
